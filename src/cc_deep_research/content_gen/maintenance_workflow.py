"""Background maintenance workflows for backlog hygiene.

This module provides recurring AI-powered maintenance jobs that run
periodically to keep the backlog healthy without operator prompting.

Maintenance jobs are read-mostly: they generate proposals that are
surfaced to the operator for review, rather than mutating state directly.

Job Types
=========
1. stale_item_review   - Items not updated in N days are flagged for refresh or archival.
2. gap_summary         - Themes or categories with low coverage are flagged for new ideas.
3. duplicate_watchlist - Items with highly similar titles/ideas are flagged for deduping.
4. rescoring_recommend  - Items whose scores are stale (>30 days) are flagged for rescoring.

All jobs output MaintenanceProposal records that are surfaced in the
dashboard as review items. Mutations require explicit operator approval.

Scheduling
==========
Jobs are scheduled via a simple in-process scheduler (threading-based)
that fires on a configurable interval. The interval is set in
ContentGenConfig.maintenance_interval_hours (default: 24 hours).
"""

from __future__ import annotations

import logging
import threading
import traceback
from datetime import UTC, datetime, timedelta
from difflib import SequenceMatcher
from enum import StrEnum
from typing import TYPE_CHECKING, Any
from uuid import uuid4

import yaml

from cc_deep_research.content_gen.backlog_service import BacklogService
from cc_deep_research.content_gen.models import BacklogItem
from cc_deep_research.content_gen.storage._paths import resolve_content_gen_file_path
from cc_deep_research.content_gen.storage.audit_store import AuditActor, AuditEventType, AuditStore

if TYPE_CHECKING:
    from cc_deep_research.config import Config

logger = logging.getLogger(__name__)

