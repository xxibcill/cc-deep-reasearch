def _similarity(a: str, b: str) -> float:
    """Return a 0-1 similarity score between two strings."""
    return SequenceMatcher(None, a.lower(), b.lower()).ratio()


class MaintenanceJobs:
    """Collection of maintenance job analyzers.

    Each job is a method that takes the backlog items and config,
    and returns a list of MaintenanceProposal objects.
    """

    def __init__(self, config: Config | None = None) -> None:
        self._config = config
        self._backlog_service = BacklogService(config)

    # -------------------------------------------------------------------------
    # Job 1: Stale item review
    # -------------------------------------------------------------------------

    def run_stale_item_review(
        self,
        stale_days: int = 60,
        watch_days: int = 30,
    ) -> list[MaintenanceProposal]:
        """Find items not meaningfully updated in stale_days.

        Items not updated in `stale_days` are flagged for archival review.
        Items updated between `watch_days` and `stale_days` are flagged
        for enrichment (fresh look).
        """
        proposals: list[MaintenanceProposal] = []
        backlog = self._backlog_service.load()
        now = datetime.now(tz=UTC)
        stale_cutoff = now - timedelta(days=stale_days)
        watch_cutoff = now - timedelta(days=watch_days)

        for item in backlog.items:
            if item.status == "archived" or item.production_status == "ready_to_publish":
                continue

            last_updated = item.updated_at
            if not last_updated:
                last_updated = item.created_at
            if not last_updated:
                continue

            try:
                updated_dt = datetime.fromisoformat(last_updated)
                if updated_dt.tzinfo is None:
                    updated_dt = updated_dt.replace(tzinfo=UTC)
            except (ValueError, TypeError):
                continue

            if updated_dt < stale_cutoff:
                proposals.append(
                    MaintenanceProposal(
                        job_type=MaintenanceJobType.STALE_ITEM_REVIEW,
                        title=f"Stale item: {item.title or item.idea}",
                        description=(
                            f"This item has not been updated since {updated_dt.strftime('%Y-%m-%d')} "
                            f"({stale_days}+ days ago). Consider archiving it if it's no longer relevant."
                        ),
                        affected_idea_ids=[item.idea_id],
                        suggested_patch={"status": "archived"},
                        priority=3,
                    )
                )
            elif updated_dt < watch_cutoff:
                proposals.append(
                    MaintenanceProposal(
                        job_type=MaintenanceJobType.STALE_ITEM_REVIEW,
                        title=f"Needs refresh: {item.title or item.idea}",
                        description=(
                            f"This item was last updated {updated_dt.strftime('%Y-%m-%d')} "
                            f"({watch_days}-{stale_days} days ago). A fresh look or new evidence "
                            "might improve its quality."
                        ),
                        affected_idea_ids=[item.idea_id],
                        suggested_patch={},  # No auto-patch; just surface for review
                        priority=1,
                    )
                )

        return proposals

    # -------------------------------------------------------------------------
    # Job 2: Gap summary
    # -------------------------------------------------------------------------

    def run_gap_summary(self, min_items_per_theme: int = 3) -> list[MaintenanceProposal]:
        """Identify themes or categories with low coverage.

        Groups backlog items by source_theme or category and flags
        underrepresented themes for new content ideas.
        """
        proposals: list[MaintenanceProposal] = []
        backlog = self._backlog_service.load()

        # Group by theme
        theme_items: dict[str, list[BacklogItem]] = {}
        for item in backlog.items:
            if item.status == "archived" or item.production_status == "ready_to_publish":
                continue
            theme = (item.source_theme or "").strip() or "_untitled_"
            if theme not in theme_items:
                theme_items[theme] = []
            theme_items[theme].append(item)

        active_themes = {t: items for t, items in theme_items.items() if len(items) < min_items_per_theme}

        for theme, items in active_themes.items():
            if theme == "_untitled_":
                description = (
                    f"{len(items)} items have no theme assigned. "
                    "Consider grouping them under a common theme or archiving low-quality items."
                )
            else:
                description = (
                    f'Theme "{theme}" has only {len(items)} active item(s). '
                    "Consider adding more ideas before moving to production."
                )
            proposals.append(
                MaintenanceProposal(
                    job_type=MaintenanceJobType.GAP_SUMMARY,
                    title=f'Gap: under-covered theme "{theme}"',
                    description=description,
                    affected_idea_ids=[item.idea_id for item in items],
                    suggested_patch={},
                    priority=2,
                )
            )

        return proposals

    # -------------------------------------------------------------------------
    # Job 3: Duplicate watchlist
    # -------------------------------------------------------------------------

    def run_duplicate_watchlist(
        self,
        similarity_threshold: float = 0.85,
        max_items: int = 50,
    ) -> list[MaintenanceProposal]:
        """Find pairs of items with highly similar titles or ideas.

        Items above the similarity threshold are flagged as potential
        duplicates for operator deduping review.
        """
        proposals: list[MaintenanceProposal] = []
        backlog = self._backlog_service.load()

        active_items = [
            item
            for item in backlog.items
            if item.status != "archived" and item.production_status != "ready_to_publish"
        ][:max_items]

        seen_pairs: set[tuple[str, str]] = set()
        for i, a in enumerate(active_items):
            for b in active_items[i + 1 :]:
                key = (a.idea_id, b.idea_id)
                if key in seen_pairs:
                    continue

                title_sim = _similarity(a.title or "", b.title or "")
                idea_sim = _similarity(a.idea or "", b.idea or "")
                best_sim = max(title_sim, idea_sim)

                if best_sim >= similarity_threshold:
                    seen_pairs.add(key)
                    descriptions = [
                        f'Item A: "{a.title or a.idea}" (status={a.status})',
                        f'Item B: "{b.title or b.idea}" (status={b.status})',
                        f"Similarity: {best_sim:.0%}",
                    ]
                    if title_sim > idea_sim:
                        descriptions.append("(High title similarity)")
                    else:
                        descriptions.append("(High idea similarity)")

                    proposals.append(
                        MaintenanceProposal(
                            job_type=MaintenanceJobType.DUPLICATE_WATCHLIST,
                            title=f"Potential duplicate pair: {a.title or a.idea[:30]}",
                            description=" | ".join(descriptions),
                            affected_idea_ids=[a.idea_id, b.idea_id],
                            suggested_patch={},  # Operator decides which to keep
                            priority=2,
                        )
                    )

        return proposals

    # -------------------------------------------------------------------------
    # Job 4: Rescoring recommendations
    # -------------------------------------------------------------------------

    def run_rescoring_recommend(
        self,
        stale_score_days: int = 30,
    ) -> list[MaintenanceProposal]:
        """Find items with stale scores that should be re-evaluated.

        Items that have been scored but not re-scored in a long time
        are flagged for rescoring in the next pipeline run.
        """
        proposals: list[MaintenanceProposal] = []
        backlog = self._backlog_service.load()
        now = datetime.now(tz=UTC)
        stale_cutoff = now - timedelta(days=stale_score_days)

        for item in backlog.items:
            if item.status == "archived" or item.production_status != "idle":
                continue
            if not item.last_scored_at:
                continue

            try:
                scored_dt = datetime.fromisoformat(item.last_scored_at)
                if scored_dt.tzinfo is None:
                    scored_dt = scored_dt.replace(tzinfo=UTC)
            except (ValueError, TypeError):
                continue

            if scored_dt < stale_cutoff:
                proposals.append(
                    MaintenanceProposal(
                        job_type=MaintenanceJobType.RESCORING_RECOMMEND,
                        title=f"Stale score: {item.title or item.idea}",
                        description=(
                            f"This item was last scored on {scored_dt.strftime('%Y-%m-%d')} "
                            f"({stale_score_days}+ days ago). "
                            "Its scores may no longer reflect current relevance."
                        ),
                        affected_idea_ids=[item.idea_id],
                        suggested_patch={},  # Rescoring happens in pipeline; just surface for awareness
                        priority=1,
                    )
                )

        return proposals


