class MaintenanceStore:
    """Persist maintenance proposals and run history.

    Stored as YAML files alongside the backlog:
    - maintenance_proposals.yaml  - pending and recent proposals
    - maintenance_runs.yaml         - history of maintenance runs
    """

    def __init__(self, config: Config | None = None) -> None:
        self._proposals_path = resolve_content_gen_file_path(
            explicit_path=None,
            config=config,
            config_attr="backlog_path",
            default_name="maintenance_proposals.yaml",
            use_config_parent=True,
        )
        self._runs_path = resolve_content_gen_file_path(
            explicit_path=None,
            config=config,
            config_attr="backlog_path",
            default_name="maintenance_runs.yaml",
            use_config_parent=True,
        )
        self._proposals_path.parent.mkdir(parents=True, exist_ok=True)

    # Proposals

    def load_proposals(
        self,
        status: MaintenanceProposalStatus | None = None,
        job_type: MaintenanceJobType | None = None,
        limit: int = 100,
    ) -> list[MaintenanceProposal]:
        """Load maintenance proposals with optional filtering."""
        if not self._proposals_path.exists():
            return []

        data = yaml.safe_load(self._proposals_path.read_text()) or []
        proposals = [MaintenanceProposal.from_dict(p) for p in data]

        if status:
            proposals = [p for p in proposals if p.status == status]
        if job_type:
            proposals = [p for p in proposals if p.job_type == job_type]

        proposals.sort(key=lambda p: (p.priority, p.created_at), reverse=True)
        return proposals[:limit]

    def save_proposal(self, proposal: MaintenanceProposal) -> None:
        """Add or update a maintenance proposal."""
        data: list[dict[str, Any]] = []
        if self._proposals_path.exists():
            data = yaml.safe_load(self._proposals_path.read_text()) or []

        # Replace if same proposal_id exists, otherwise append
        replaced = False
        for i, p in enumerate(data):
            if p.get("proposal_id") == proposal.proposal_id:
                data[i] = proposal.to_dict()
                replaced = True
                break
        if not replaced:
            data.append(proposal.to_dict())

        self._proposals_path.write_text(yaml.dump(data, default_flow_style=False, sort_keys=False))

    def resolve_proposal(
        self,
        proposal_id: str,
        decision: str,  # "approved" | "rejected"
        reviewed_by: str = "",
    ) -> MaintenanceProposal | None:
        """Resolve a proposal (approve or reject)."""
        if not self._proposals_path.exists():
            return None

        data: list[dict[str, Any]] = yaml.safe_load(self._proposals_path.read_text()) or []
        for p in data:
            if p.get("proposal_id") == proposal_id:
                p["status"] = (
                    MaintenanceProposalStatus.APPROVED.value
                    if decision == "approved"
                    else MaintenanceProposalStatus.REJECTED.value
                )
                p["reviewed_at"] = datetime.now(tz=UTC).isoformat()
                p["reviewed_by"] = reviewed_by
                p["outcome"] = decision
                proposal = MaintenanceProposal.from_dict(p)
                self._proposals_path.write_text(yaml.dump(data, default_flow_style=False, sort_keys=False))
                return proposal
        return None

    def expire_proposal(self, proposal_id: str) -> None:
        """Mark a proposal as expired."""
        if not self._proposals_path.exists():
            return

        data: list[dict[str, Any]] = yaml.safe_load(self._proposals_path.read_text()) or []
        for p in data:
            if p.get("proposal_id") == proposal_id:
                p["status"] = MaintenanceProposalStatus.EXPIRED.value
                p["reviewed_at"] = datetime.now(tz=UTC).isoformat()
                p["outcome"] = "expired"
                break
        self._proposals_path.write_text(yaml.dump(data, default_flow_style=False, sort_keys=False))

    # Runs

    def save_run(self, run: MaintenanceRun) -> None:
        """Append a maintenance run record."""
        data: list[dict[str, Any]] = []
        if self._runs_path.exists():
            data = yaml.safe_load(self._runs_path.read_text()) or []
        data.append(run.to_dict())
        # Keep last 100 runs
        if len(data) > 100:
            data = data[-100:]
        self._runs_path.write_text(yaml.dump(data, default_flow_style=False, sort_keys=False))

    def load_runs(self, limit: int = 20) -> list[MaintenanceRun]:
        """Load recent maintenance run records."""
        if not self._runs_path.exists():
            return []
        data: list[dict[str, Any]] = yaml.safe_load(self._runs_path.read_text()) or []
        runs = [MaintenanceRun(**r) if isinstance(r, dict) else r for r in data]
        runs.sort(key=lambda r: r.started_at, reverse=True)
        return runs[:limit]


