class MaintenanceJobType(StrEnum):
    """Types of recurring maintenance jobs."""

    STALE_ITEM_REVIEW = "stale_item_review"
    GAP_SUMMARY = "gap_summary"
    DUPLICATE_WATCHLIST = "duplicate_watchlist"
    RESCORING_RECOMMEND = "rescoring_recommend"


class MaintenanceProposalStatus(StrEnum):
    """Status of a maintenance proposal."""

    PENDING = "pending"  # Awaiting operator review
    APPROVED = "approved"  # Operator approved, ready to apply
    REJECTED = "rejected"  # Operator rejected
    APPLIED = "applied"  # Successfully applied
    EXPIRED = "expired"  # No longer relevant (item deleted, etc.)


class MaintenanceProposal:
    """A single maintenance proposal surfaced to the operator for review.

    Proposals are generated by maintenance jobs but require operator
    approval before any backlog mutation is applied.
    """

    def __init__(
        self,
        proposal_id: str = "",
        job_type: MaintenanceJobType | str = "",
        title: str = "",
        description: str = "",
        affected_idea_ids: list[str] | None = None,
        suggested_patch: dict[str, Any] | None = None,
        priority: int = 0,
        status: MaintenanceProposalStatus = MaintenanceProposalStatus.PENDING,
        created_at: str = "",
        reviewed_at: str = "",
        reviewed_by: str = "",
        outcome: str = "",
    ) -> None:
        self.proposal_id = proposal_id or f"mnt_{uuid4().hex[:10]}"
        self.job_type = MaintenanceJobType(job_type) if job_type and isinstance(job_type, str) else job_type
        self.title = title
        self.description = description
        self.affected_idea_ids = affected_idea_ids or []
        self.suggested_patch = suggested_patch or {}
        self.priority = priority
        self.status = status
        self.created_at = created_at or datetime.now(tz=UTC).isoformat()
        self.reviewed_at = reviewed_at
        self.reviewed_by = reviewed_by
        self.outcome = outcome

    def to_dict(self) -> dict[str, Any]:
        job_type_val = self.job_type.value if isinstance(self.job_type, MaintenanceJobType) else str(self.job_type or "")
        return {
            "proposal_id": self.proposal_id,
            "job_type": job_type_val,
            "title": self.title,
            "description": self.description,
            "affected_idea_ids": self.affected_idea_ids,
            "suggested_patch": self.suggested_patch,
            "priority": self.priority,
            "status": str(self.status.value),
            "created_at": self.created_at,
            "reviewed_at": self.reviewed_at,
            "reviewed_by": self.reviewed_by,
            "outcome": self.outcome,
        }

    @classmethod
    def from_dict(cls, data: dict[str, Any]) -> MaintenanceProposal:
        return cls(
            proposal_id=data.get("proposal_id", ""),
            job_type=data.get("job_type", ""),
            title=data.get("title", ""),
            description=data.get("description", ""),
            affected_idea_ids=data.get("affected_idea_ids", []),
            suggested_patch=data.get("suggested_patch", {}),
            priority=data.get("priority", 0),
            status=data.get("status", MaintenanceProposalStatus.PENDING),
            created_at=data.get("created_at", ""),
            reviewed_at=data.get("reviewed_at", ""),
            reviewed_by=data.get("reviewed_by", ""),
            outcome=data.get("outcome", ""),
        )


class MaintenanceRun:
    """Record of a single maintenance job execution."""

    def __init__(
        self,
        run_id: str = "",
        job_type: MaintenanceJobType | str = "",
        proposals_count: int = 0,
        started_at: str = "",
        completed_at: str = "",
        outcome: str = "success",
        error: str = "",
    ) -> None:
        self.run_id = run_id or f"mntrun_{uuid4().hex[:10]}"
        self.job_type = MaintenanceJobType(job_type) if job_type and isinstance(job_type, str) else job_type
        self.proposals_count = proposals_count
        self.started_at = started_at or datetime.now(tz=UTC).isoformat()
        self.completed_at = completed_at or datetime.now(tz=UTC).isoformat()
        self.outcome = outcome
        self.error = error

    def to_dict(self) -> dict[str, Any]:
        job_type_val = self.job_type.value if isinstance(self.job_type, MaintenanceJobType) else str(self.job_type or "")
        return {
            "run_id": self.run_id,
            "job_type": job_type_val,
            "proposals_count": self.proposals_count,
            "started_at": self.started_at,
            "completed_at": self.completed_at,
            "outcome": self.outcome,
            "error": self.error,
        }


