class MaintenanceScheduler:
    """In-process scheduler for recurring maintenance jobs.

    Uses a background thread with a threading.Timer loop to run
    maintenance jobs on a configurable interval.

    Jobs are read-mostly: they generate proposals surfaced to the
    operator, not direct mutations. Mutations require approval.
    """

    def __init__(
        self,
        config: Config | None = None,
        interval_hours: float = 24.0,
    ) -> None:
        self._config = config
        self._interval_hours = interval_hours
        self._timer: threading.Timer | None = None
        self._running = False
        self._lock = threading.Lock()
        self._audit_store = AuditStore(config=config)

    def start(self) -> None:
        """Start the maintenance scheduler in a background thread."""
        with self._lock:
            if self._running:
                return
            self._running = True
            self._schedule_next()

    def stop(self) -> None:
        """Stop the scheduler."""
        with self._lock:
            self._running = False
            if self._timer is not None:
                self._timer.cancel()
                self._timer = None

    def _schedule_next(self) -> None:
        """Schedule the next maintenance run."""
        interval_ms = int(self._interval_hours * 3600 * 1000)
        self._timer = threading.Timer(interval_ms / 1000, self._run_jobs)
        self._timer.daemon = True
        self._timer.start()

    def _run_jobs(self) -> None:
        """Run all maintenance jobs and reschedule."""
        if not self._running:
            return

        try:
            self.run_all_jobs()
        except Exception:
            logger.exception("Maintenance job run failed")

        if self._running:
            self._schedule_next()

    def run_all_jobs(self) -> list[MaintenanceRun]:
        """Run all maintenance jobs and persist proposals.

        Returns a list of MaintenanceRun records (one per job type).
        """
        jobs = MaintenanceJobs(self._config)
        store = MaintenanceStore(self._config)
        runs: list[MaintenanceRun] = []

        job_types = [
            MaintenanceJobType.STALE_ITEM_REVIEW,
            MaintenanceJobType.GAP_SUMMARY,
            MaintenanceJobType.DUPLICATE_WATCHLIST,
            MaintenanceJobType.RESCORING_RECOMMEND,
        ]

        for job_type in job_types:
            run = MaintenanceRun(job_type=job_type)
            try:
                if job_type == MaintenanceJobType.STALE_ITEM_REVIEW:
                    proposals = jobs.run_stale_item_review()
                elif job_type == MaintenanceJobType.GAP_SUMMARY:
                    proposals = jobs.run_gap_summary()
                elif job_type == MaintenanceJobType.DUPLICATE_WATCHLIST:
                    proposals = jobs.run_duplicate_watchlist()
                elif job_type == MaintenanceJobType.RESCORING_RECOMMEND:
                    proposals = jobs.run_rescoring_recommend()
                else:
                    proposals = []

                for proposal in proposals:
                    store.save_proposal(proposal)

                run.proposals_count = len(proposals)
                run.completed_at = datetime.now(tz=UTC).isoformat()

                # Log to audit
                self._audit_store.log_backlog_mutation(
                    event_type=AuditEventType.MAINTENANCE_JOB_RUN,
                    idea_id="",
                    actor=AuditActor.MAINTENANCE,
                    patch={"job_type": job_type.value, "proposals_count": len(proposals)},
                    outcome="success",
                )
            except Exception as exc:
                run.completed_at = datetime.now(tz=UTC).isoformat()
                run.outcome = "error"
                run.error = traceback.format_exc()
                logger.exception("Maintenance job %s failed", job_type.value)

            store.save_run(run)
            runs.append(run)

        return runs

    def trigger_job(self, job_type: MaintenanceJobType) -> MaintenanceRun:
        """Trigger a single maintenance job immediately (on-demand)."""
        jobs = MaintenanceJobs(self._config)
        store = MaintenanceStore(self._config)
        run = MaintenanceRun(job_type=job_type)

        try:
            if job_type == MaintenanceJobType.STALE_ITEM_REVIEW:
                proposals = jobs.run_stale_item_review()
            elif job_type == MaintenanceJobType.GAP_SUMMARY:
                proposals = jobs.run_gap_summary()
            elif job_type == MaintenanceJobType.DUPLICATE_WATCHLIST:
                proposals = jobs.run_duplicate_watchlist()
            elif job_type == MaintenanceJobType.RESCORING_RECOMMEND:
                proposals = jobs.run_rescoring_recommend()
            else:
                proposals = []

            for proposal in proposals:
                store.save_proposal(proposal)

            run.proposals_count = len(proposals)
            run.completed_at = datetime.now(tz=UTC).isoformat()

            self._audit_store.log_backlog_mutation(
                event_type=AuditEventType.MAINTENANCE_JOB_RUN,
                idea_id="",
                actor=AuditActor.MAINTENANCE,
                patch={"job_type": job_type.value, "proposals_count": len(proposals)},
                outcome="success",
            )
        except Exception as exc:
            run.completed_at = datetime.now(tz=UTC).isoformat()
            run.outcome = "error"
            run.error = traceback.format_exc()
            logger.exception("Maintenance job %s failed", job_type.value)

        store.save_run(run)
        return run
